<h1>Dungeon Friend</h1>

<p>
  Dungeon Friend is a minimal character sheet manager for D&D. Instead of having
  a paper character sheet with tons of eraser marks and cross outs, you can use
  dungeon friend to update your character's stats, abilities, and statuses on
  the fly and save them between sessions.
